namespace Vapp.Media
{
    /// <summary>
    /// Specifices what repeat mode to use
    /// </summary>
    public enum RepeatMode
    {
        /// <summary>
        /// Don't repeat
        /// </summary>
        None,

        /// <summary>
        /// Repeat same item continuously
        /// </summary>
        Once,

        /// <summary>
        /// Repeat current list of items
        /// </summary>
        All,
    }
}